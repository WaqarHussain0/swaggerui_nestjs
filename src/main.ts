import { NestFactory } from '@nestjs/core';
import { AppModule } from './app.module';
import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger';
import { ValidationPipe } from '@nestjs/common';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);
  app.useGlobalPipes(new ValidationPipe());
  const config = new DocumentBuilder()
    .setTitle('Swagger UI')
    .setDescription(
      'A tool for generating interactive API documentation directly from your OpenAPI Specification. It allows developers to visualize and interact with the API resources without having to dig into the code. Swagger UI provides a user-friendly interface for exploring endpoints, understanding request/response payloads, and testing API operations.',
    )
    .setVersion('1.0')
    .build();
  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api', app, document);

  await app.listen(3000);
}
bootstrap();
